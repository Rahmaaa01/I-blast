    public function sendMail(Request $request){
        $receivers = explode(" ",$request->email);
        foreach($receivers as $receiver){
            $data = [
                'from' => $request->from,
                'subject' => $request->subject,
                'pesan' => $request->pesan
            ];

            Mail::to($receiver)->send(new EmailBlast($data));
        }

        return back();

    }

